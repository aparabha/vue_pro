<template>
    <form @submit.prevent="submitData">
        <div>
            <label>Name</label>
            <input type="text" v-model="enteredName"/>
        </div>
        <div>
            <label>Telephone</label>
            <input type="tel" v-model="enteredPhone"/>
        </div>
        <div>
            <label>E-mail</label>
            <input type="email" v-model="enteredEmail"/>
        </div>
        <div><button>Add Contact</button></div>
    </form>
</template>
<script>
export default {
  emits:['add-contact'], 
  data(){
      return{
          enteredName:'',
          enteredPhone:'',
          enteredEmail:''
      };
  },
  methods:{
      submitData(){
         console.log(this.enteredName);
          this.$emit('add-contact',
                    this.enteredName,
                    this.enteredPhone,
                    this.enteredEmail);

      }
  }
}
</script>
